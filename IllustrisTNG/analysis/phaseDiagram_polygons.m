function polys=phaseDiagram_polygons(plotInd)
%% define polygons in phase diagram

if ~exist('plotInd','var')
    plotFlag=false;
else
    plotFlag=strcmp(plotInd,'plot') | strcmp(plotInd,'show');
end

minT=3;
maxT=9.0;

minRo= -6.5;
maxRo= 2.0;

denseDilute=-3.5;
coldWarm=5.0;
hotTemp=6.5;

polys.minT=3.8785;
polys.maxT=8.5;

polys.minRo= -6.035;
polys.maxRo= 1.04;

polys.denseDilute=-3.5;
polys.coldWarm=5.0;
polys.hotTemp=6.5;

% star forming gas

polys.sf_polygonMask=[...
    1.04     4.8854;...
    1.04    4.7971;...
    -0.3536    4.1350;...
    -0.4926    4.175;...
    -0.6335    4.175;...
    -0.6335    4.199;...
    -0.57      4.205;...
    -0.557     4.225;...
    -0.4698    4.2250;...
    -0.4679    4.2356;...
    0.9715    4.8944;...
    1.04     4.8944];


% cold & dense
polys.coldDense_polygonMask=[...
    denseDilute   coldWarm;...
    maxRo        coldWarm;...
    maxRo        minT;...
    denseDilute  minT];


polys.coldDense_polygonMask2=[...
    denseDilute   coldWarm;...
    maxRo        coldWarm;...
    maxRo        4.8944;...
    0.9715       4.8944;...
    -0.4679       4.2356;...
    -0.4698       4.2250;...
    -0.557        4.225;...
    -0.57         4.205;...
    -0.6335       4.199;...
    -0.6335       4.175;...
    -0.4926       4.175;...
    -0.3536       4.1350;...
    maxRo        4.7971;...
    maxRo        minT;...
    denseDilute  minT];

% cold and dilute
polys.coldDilute_polygonMask=[...
    minRo        coldWarm;...
    denseDilute  coldWarm;...
    denseDilute  minT;...
    minRo        minT];


% warm -Hot
polys.warmHot_polygonMask=[
    minRo    coldWarm;...
    maxRo    coldWarm;...
    maxRo    maxT;...
    minRo    maxT];

% hot by temp
polys.hotTemp_polygonMask=[
    minRo    hotTemp;...
    maxRo     hotTemp;...
    maxRo     maxT;...
    minRo    maxT];


% above Tvir.
%hotTvir_polygonMask

if plotFlag
    figure
    fill(polys.coldDense_polygonMask(:,1),polys.coldDense_polygonMask(:,2),'b')
    hold on
    
    fill(polys.sf_polygonMask(:,1),polys.sf_polygonMask(:,2),'g')
    
    fill(polys.coldDilute_polygonMask(:,1),polys.coldDilute_polygonMask(:,2),'c')
    fill(polys.warmHot_polygonMask(:,1),polys.warmHot_polygonMask(:,2),'m')
    fill(polys.hotTemp_polygonMask(:,1),polys.hotTemp_polygonMask(:,2),'r')
    
    xlim([minRo maxRo])
    ylim([minT maxT])
    
end

end
